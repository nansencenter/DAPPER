# https://stackoverflow.com/a/22434594
def cap_stdout(fun, *args, **kwargs):
    """Capture stdout."""
    with io.StringIO() as stringbuf, redirect_stdout(stringbuf):
        fun(*args, **kwargs)
        return stringbuf.getvalue()


